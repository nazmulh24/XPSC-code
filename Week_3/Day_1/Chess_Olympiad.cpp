void solve()
{
    int x, y, z;
    cin >> x >> y >> z;

    float cnt1 = 0, cnt2 = 0;

    cnt1 = (4 - x - y - z) + x + y * 0.5;
    cnt2 = z + y * 0.5;

    // cout << cnt2 << endl;

    if (cnt1 > cnt2)
        yes;
    else
        no;
}

