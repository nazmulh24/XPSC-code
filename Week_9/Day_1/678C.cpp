void solve()
{
    int n, a, b, p, q;
    cin >> n >> a >> b >> p >> q;

    int cnt1 = (n / a) * p, cnt2 = (n / b) * q, overlap = (n / lcm(a, b));

    int ans = (cnt1 + cnt2 - overlap * (p + q)) + (overlap * max(p, q));

    cout << ans << endl;
}

